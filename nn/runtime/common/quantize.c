#include "quantize.h"
#include <math.h>
/* ============================ [ MACROS    ] ====================================================== */
/* ============================ [ TYPES     ] ====================================================== */
/* ============================ [ DECLARES  ] ====================================================== */
/* ============================ [ DATAS     ] ====================================================== */
/* ============================ [ LOCALS    ] ====================================================== */
/* ============================ [ FUNCTIONS ] ====================================================== */
void dequantize_q16(float* out, int16_t* in, size_t n, int Q)
{
	size_t i;

	for(i=0; i<n; i++)
	{
		out[i] = (float)in[i]/pow(2,Q);
	}
}
